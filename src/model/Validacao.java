package model;

public class Validacao {
	private int id_validacao;
	private String tipoValidacao;
	private String valorFixo;
	private String tabelaCondicao;
	private String campoCondicao;
	private String operacaoCondicao;
	private String valorCondicao;
	private int id_campo;
	
	public int getId_validacao() {
		return id_validacao;
	}
	public void setId_validacao(int id_validacao) {
		this.id_validacao = id_validacao;
	}
	public String getTipoValidacao() {
		return tipoValidacao;
	}
	public void setTipoValidacao(String tipoValidacao) {
		this.tipoValidacao = tipoValidacao;
	}
	public String getValorFixo() {
		return valorFixo;
	}
	public void setValorFixo(String valorFixo) {
		this.valorFixo = valorFixo;
	}
	public String getTabelaCondicao() {
		return tabelaCondicao;
	}
	public void setTabelaCondicao(String tabelaCondicao) {
		this.tabelaCondicao = tabelaCondicao;
	}
	public String getCampoCondicao() {
		return campoCondicao;
	}
	public void setCampoCondicao(String campoCondicao) {
		this.campoCondicao = campoCondicao;
	}
	public String getOperacaoCondicao() {
		return operacaoCondicao;
	}
	public void setOperacaoCondicao(String operacaoCondicao) {
		this.operacaoCondicao = operacaoCondicao;
	}
	public String getValorCondicao() {
		return valorCondicao;
	}
	public void setValorCondicao(String valorCondicao) {
		this.valorCondicao = valorCondicao;
	}
	public int getId_campo() {
		return id_campo;
	}
	public void setId_campo(int id_campo) {
		this.id_campo = id_campo;
	}
	
	

}
